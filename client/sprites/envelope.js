function drawEnvelopeSprite(ctx, x, y) {
    // Define the color palette
    const envelopeColors = [
        'transparent',                  // 0: transparent
        '#A37A7A',                      // 1: envelope outline
        '#D4A7A7',                      // 2: envelope secondary
        '#C7B5B5',                      // 3: envelope shadow
        '#FFF2F2',                      // 4: envelope body
    ];
    envelopeSprite.forEach((row, i) => {
        row.forEach((pixel, j) => {
            if (pixel !== 0) {
                ctx.fillStyle = envelopeColors[pixel];
                ctx.fillRect(x + j, y + i, 1, 1);
            }
        });
    });
}

export { drawEnvelopeSprite };